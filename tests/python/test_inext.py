def test_inext_q0_interpolation_pins_two():
    # Cross-check against the existing rarefaction pin: [1,1,1,1] has n=4, S=4,
    # E[S] at subsample m=2 is 4*(1 - C(3,2)/C(4,2)) = 4*(1 - 3/6) = 2.0.
    df = stats.inext([1, 1, 1, 1], q=0, sizes=[2], se=False)
    assert df["method"][0] == "rarefaction"
    assert math.isclose(df["qD"][0], 2.0, rel_tol=1e-12)


def test_chat_tiny_vector():
    # x = [1, 1, 2]: n=4, f1=2, f2=1. Chat(n) = 1 - (f1/n)*((n-1)*f1/((n-1)*f1+2*f2))
    #   = 1 - (2/4)*((3*2)/(3*2+2)) = 1 - 0.5*(6/8) = 1 - 0.375 = 0.625.
    assert math.isclose(stats.sample_coverage([1, 1, 2]), 0.625, rel_tol=1e-12)


def test_q2_closed_form_hand():
    # x = [2, 2]: n=4. D2(m) = 1/(1/m + (m-1)/m * sum X(X-1)/(n(n-1))).
    # sum X(X-1) = 2+2 = 4; n(n-1) = 12; term = 4/12 = 1/3.
    # At m=n=4 (observed): plug-in inverse-Simpson = 1/((0.5)^2+(0.5)^2) = 2.0.
    df = stats.inext([2, 2], q=2, sizes=[4], se=False)
    assert math.isclose(df["qD"][0], 2.0, rel_tol=1e-12)
    # At m=2: 1/(1/2 + (1/2)*(1/3)) = 1/(0.5 + 0.16667) = 1.5.
    df2 = stats.inext([2, 2], q=2, sizes=[2], se=False)
    assert math.isclose(df2["qD"][0], 1.5, rel_tol=1e-12)


def test_observed_row_equals_plugin(girdled):
    # The observed (m=n) row must equal the plug-in Hill number of each order.
    df = stats.inext(girdled, q=(0, 1, 2), sizes=[168], se=False)
    p = girdled / girdled.sum()
    plugin = {
        0: float((p > 0).sum()),
        1: float(np.exp(-np.sum(p * np.log(p)))),
        2: float(1.0 / np.sum(p**2)),
    }
    for r in df.iter_rows(named=True):
        assert r["method"] == "observed"
        assert math.isclose(r["qD"], plugin[r["order_q"]], rel_tol=1e-12)


def test_q0_monotone_nondecreasing(girdled):
    # Richness (q=0) is non-decreasing in sampling depth m.
    df = stats.inext(girdled, q=0, knots=25, se=False).sort("m")
    vals = df["qD"].to_list()
    assert all(b >= a - 1e-9 for a, b in zip(vals, vals[1:]))


def test_dataframe_input_uses_duplicate_count(girdled):
    # A clonotype DataFrame with duplicate_count yields the same curve as the vector.
    frame = pl.DataFrame({"duplicate_count": girdled})
    a = stats.inext(girdled, q=0, sizes=[100], se=False)["qD"][0]
    b = stats.inext(frame, q=0, sizes=[100], se=False)["qD"][0]
    assert math.isclose(a, b, rel_tol=1e-12)
