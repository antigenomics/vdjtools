"""Tests for vdjtools.stats.inext — numerically pinned to the iNEXT R package.

The oracle values below are the DETERMINISTIC point estimates of iNEXT 3.0.2 on
the ``spider`` abundance vectors (``Girdled`` / ``Logged``), regenerated by
``tests/assets/inext_oracle.R`` and committed as ``tests/assets/spider_*.txt``.
The tests read the committed vectors + hardcoded constants only — R is NOT
required at test time. Confidence intervals are RNG-dependent, so only their
structure is asserted (plus one loose se sanity check).
"""
import math
from pathlib import Path

import numpy as np
import polars as pl
import pytest

from vdjtools import stats

ASSETS = Path(__file__).resolve().parent.parent / "assets"


def _load(name: str) -> np.ndarray:
    text = (ASSETS / name).read_text()
    return np.array([int(t) for t in text.split()], dtype=np.int64)


@pytest.fixture(scope="module")
def girdled() -> np.ndarray:
    return _load("spider_girdled.txt")


@pytest.fixture(scope="module")
def logged() -> np.ndarray:
    return _load("spider_logged.txt")


RTOL = 1e-4  # point estimates are deterministic in iNEXT


# --------------------------------------------------------------------------- #
# DataInfo: n, S_obs, f1, f2, and observed sample coverage Chat(n)
# --------------------------------------------------------------------------- #
def test_datainfo_girdled(girdled):
    assert int(girdled.sum()) == 168
    assert girdled.size == 26
    assert int(np.count_nonzero(girdled == 1)) == 12
    assert int(np.count_nonzero(girdled == 2)) == 4
    assert math.isclose(stats.sample_coverage(girdled), 0.9288554, rel_tol=RTOL)


def test_datainfo_logged(logged):
    assert int(logged.sum()) == 252
    assert logged.size == 37
    assert int(np.count_nonzero(logged == 1)) == 14
    assert int(np.count_nonzero(logged == 2)) == 4
    assert math.isclose(stats.sample_coverage(logged), 0.9446, rel_tol=RTOL)


# --------------------------------------------------------------------------- #
# Coverage Chat(m)
# --------------------------------------------------------------------------- #
def test_coverage_girdled(girdled):
    assert math.isclose(stats.sample_coverage(girdled, 100), 0.9072004, rel_tol=RTOL)
    assert math.isclose(stats.sample_coverage(girdled, 200), 0.9373713, rel_tol=RTOL)
    assert math.isclose(stats.sample_coverage(girdled, 336), 0.9635701, rel_tol=RTOL)


def test_coverage_frame(girdled):
    frame = stats.sample_coverage(girdled, [100, 200, 336])
    assert frame.columns == ["m", "sample_coverage"]
    assert math.isclose(frame["sample_coverage"][1], 0.9373713, rel_tol=RTOL)


