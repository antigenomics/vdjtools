# (m, q) -> expected qD, from iNEXT size_based table (Girdled).
GIRDLED_SIZE = {
    (100, 0): 20.459426, (100, 1): 11.266546, (100, 2): 7.627561,
    (168, 0): 26.000000, (168, 1): 12.059650, (168, 2): 7.840000,
    (200, 0): 28.141739, (200, 1): 12.303742, (200, 2): 7.891717,
    (336, 0): 34.730730, (336, 1): 13.016966, (336, 2): 8.003912,
}


def test_size_based_girdled(girdled):
    df = stats.inext(girdled, q=(0, 1, 2), sizes=[100, 168, 200, 336], se=False)
    got = {(r["m"], r["order_q"]): r["qD"] for r in df.iter_rows(named=True)}
    for key, expected in GIRDLED_SIZE.items():
        assert math.isclose(got[key], expected, rel_tol=RTOL), f"{key}: {got[key]}"


def test_size_based_girdled_methods(girdled):
    df = stats.inext(girdled, q=0, sizes=[100, 168, 200, 336], se=False).sort("m")
    assert df["method"].to_list() == [
        "rarefaction", "observed", "extrapolation", "extrapolation"]
    # sample_coverage attached per depth
    assert math.isclose(
        df.filter(pl.col("m") == 200)["sample_coverage"][0], 0.9373713, rel_tol=RTOL)


