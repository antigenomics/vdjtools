# --------------------------------------------------------------------------- #
# Confidence-interval STRUCTURE (bounds only — RNG differs from R)
# --------------------------------------------------------------------------- #
def test_ci_structure_size(girdled):
    df = stats.inext(girdled, q=(0, 1, 2), sizes=[50, 100, 168, 250], nboot=30, seed=0)
    assert (df["qD_lo"] <= df["qD"]).all()
    assert (df["qD"] <= df["qD_hi"]).all()
    assert (df["qD_lo"] >= 0.0).all()
    assert ((df["sample_coverage"] >= 0.0) & (df["sample_coverage"] <= 1.0)).all()


def test_ci_structure_coverage(girdled):
    df = stats.inext_coverage(girdled, q=(0, 1, 2), coverages=[0.9, 0.95], nboot=30, seed=0)
    assert (df["qD_lo"] <= df["qD"]).all()
    assert (df["qD"] <= df["qD_hi"]).all()
    assert (df["qD_lo"] >= 0.0).all()


def test_bootstrap_se_loose(girdled):
    # iNEXT (nboot=200, seed=0) reports se ~ 1.626 for Girdled size m=100, q=0.
    # numpy's RNG differs, so only require agreement within ~40%.
    df = stats.inext(girdled, q=0, sizes=[100], nboot=200, seed=0)
    se = (df["qD_hi"][0] - df["qD"][0]) / 1.959964
    assert 0.6 * 1.626 <= se <= 1.4 * 1.626


